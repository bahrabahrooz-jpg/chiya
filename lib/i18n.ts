// Kurdish (Sorani) — language names only; everything else falls back to English.
const ku: Dict = {
  "lang.en": "ئینگلیزی",
  "lang.ar": "عەرەبی",
  "lang.ku": "کوردی",
};

export const TRANSLATIONS: Record<Lang, Dict> = { en, ar, ku };

/** Minified inline script for <head> — sets dir/lang before first paint. */
export const LANG_INIT_SCRIPT = `(function(){try{var k="${LANG_KEY}";var l=localStorage.getItem(k);if(l!=="en"&&l!=="ar"&&l!=="ku")l="en";var e=document.documentElement;e.lang=l;e.dir=(l==="ar"||l==="ku")?"rtl":"ltr";}catch(e){}})();`;

/** Translate a key for a given language, falling back to English then the key. */
export function translate(lang: Lang, key: string): string {
  return TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
}

/* ---- external store ---- */

const listeners = new Set<() => void>();
function emit() {
  listeners.forEach((l) => l());
}

function subscribe(onChange: () => void) {
  listeners.add(onChange);
  const onStorage = (e: StorageEvent) => {
    if (e.key === LANG_KEY) emit();
  };
  window.addEventListener("storage", onStorage);
  return () => {
    listeners.delete(onChange);
    window.removeEventListener("storage", onStorage);
  };
}

function getSnapshot(): Lang {
  const l = document.documentElement.lang;
  return l === "ar" || l === "ku" ? l : "en";
}
function getServerSnapshot(): Lang {
  return "en";
}

function applyLang(lang: Lang) {
  const el = document.documentElement;
  el.lang = lang;
  el.dir = dirFor(lang);
}

/**
 * useLang — reactive language hook: active language, a setter (persists +
 * updates <html dir/lang>), the current direction, and a bound translate fn.
 */
export function useLang() {
  const lang = useSyncExternalStore(subscribe, getSnapshot, getServerSnapshot);

  const setLang = useCallback((next: Lang) => {
    try {
      localStorage.setItem(LANG_KEY, next);
    } catch {
      /* noop */
    }
    applyLang(next);
    emit();
  }, []);

  const t = useCallback((key: string) => translate(lang, key), [lang]);

  return { lang, setLang, dir: dirFor(lang), t };
}
